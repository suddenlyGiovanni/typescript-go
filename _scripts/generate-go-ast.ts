function generateKind(): string {
    const w = new CodeWriter();
    w.write("// Code generated by _scripts/generate-go-ast.ts. DO NOT EDIT.");
    w.write("");
    w.write("package ast");
    w.write("");
    w.write("//go:generate go tool golang.org/x/tools/cmd/stringer -type=Kind -output=kind_stringer_generated.go");
    w.write("//go:generate npx dprint fmt kind_stringer_generated.go");
    w.write("");
    w.write("type Kind int16");
    w.write("");
    w.write("const (");
    w.push();

    const elements = api.kindElements();
    const markers = api.kindMarkers();
    const kindUnions = api.kindAliases();

    // Build set of all defined kind names for validation
    const definedKinds = new Set<string>();
    for (const element of elements) {
        if (element.name) {
            definedKinds.add(element.name);
        }
    }

    // Validate marker references (markers can reference kind names or other marker names)
    const markerNames = new Set(markers.map(m => m.name));
    for (const marker of markers) {
        if (!definedKinds.has(marker.value) && !markerNames.has(marker.value)) {
            throw new Error(`Kind marker ${marker.name} references undefined kind or marker "${marker.value}"`);
        }
    }

    // Validate union references
    for (const kindUnion of kindUnions) {
        for (const m of kindUnion.members) {
            if (!definedKinds.has(m) && !api.hasKindAlias(m)) {
                throw new Error(`Kind union ${kindUnion.name} references undefined kind or union "${m}"`);
            }
        }
    }

    // Emit elements
    let first = true;
    for (const el of elements) {
        if (!el.name) {
            w.write(`// ${el.comment}`);
            continue;
        }
        const trailing = el.comment ? ` // ${el.comment}` : "";
        if (first) {
            w.write(`Kind${el.name} Kind = iota${trailing}`);
            first = false;
        }
        else {
            w.write(`Kind${el.name}${trailing}`);
        }
    }

    // KindCount
    w.write("KindCount");

    // Markers
    const maxLen = Math.max(...markers.map(m => m.name.length));
    for (const marker of markers) {
        const padding = " ".repeat(maxLen - marker.name.length);
        w.write(`Kind${marker.name}${padding} = Kind${marker.value}`);
    }

    w.pop();
    w.write(")");
    w.write("");

    // Kind union type aliases
    if (kindUnions.length > 0) {
        w.write("type (");
        w.push();
        for (const { name, members } of kindUnions) {
            function expandMembers(ms: string[]): string[] {
                const result: string[] = [];
                for (const m of ms) {
                    const alias = api.getKindAlias(m);
                    if (alias) {
                        result.push(...expandMembers(alias.members));
                    }
                    else {
                        result.push(m);
                    }
                }
                return result;
            }
            const expanded = expandMembers(members);
            const comment = expanded.map(m => `Kind${m}`).join(" | ");
            w.write(`${name} = Kind // ${comment}`);
        }
        w.pop();
        w.write(")");
        w.write("");
    }

    return w.toString();
}

function writeAndFormat(filePath: string, content: string) {
    fs.writeFileSync(filePath, content);
    execaSync("dprint", ["fmt", filePath], { stdio: "inherit", cwd: ROOT });
    console.log(`Wrote ${filePath}`);
}

export default function main() {
    console.log("Generating Go AST code...");

    const code = generate();
    const outPath = path.join(ROOT, "internal/ast/ast_generated.go");
    writeAndFormat(outPath, code + "\n");

    const kindCode = generateKind();
    const kindOutPath = path.join(ROOT, "internal/ast/kind_generated.go");
    writeAndFormat(kindOutPath, kindCode + "\n");
}

